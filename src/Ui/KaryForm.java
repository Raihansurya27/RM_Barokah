    private void tambahFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahFotoActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
         fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
         FileNameExtensionFilter filter = new FileNameExtensionFilter("*.IMAGE", "jpg","png");
         

         fileChooser.addChoosableFileFilter(filter);
         int result = fileChooser.showSaveDialog(null);
         if(result == JFileChooser.APPROVE_OPTION){
             File selectedFile = fileChooser.getSelectedFile();
             String path = selectedFile.getAbsolutePath();
             Path paths = Paths.get(path);
            try {
                long bytes = Files.size(paths);
                DecimalFormat formatData = new DecimalFormat("#.##");
                double a;
                a = Double.valueOf(formatData.format((float)bytes/1000000));
                if(a > 2.048){
                    JOptionPane.showMessageDialog(rootPane, "Ukuran file gambar terlalu besar."+"\nUkuran file = "+(a)+" Mb");
                }else{
                    gambarLbl.setIcon(ubahukuran(path));
                    
                    setGambar(path);
                    ukuranLbl.setText(String.valueOf(a)+" Mb");
                }
            } catch (IOException ex) {
                Logger.getLogger(MenuForm.class.getName()).log(Level.SEVERE, null, ex);
            }
             
              }
         else if(result == JFileChooser.CANCEL_OPTION){
             System.out.println("No Data");
         }
    }//GEN-LAST:event_tambahFotoActionPerformed

