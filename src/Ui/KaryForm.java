    private void tambahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBtnActionPerformed
    if(!this.getIdTxt().getText().trim().isEmpty()&
            !this.getNamaTxt().getText().trim().isEmpty() & 
            !this.getAlamatTxtArea().getText().trim().isEmpty()){    
        try {
            controller.insert();
        } catch (IOException ex) {
            Logger.getLogger(MenuForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }else{
        JOptionPane.showMessageDialog(rootPane, "Mohon diisi semuanya");
    }
    }//GEN-LAST:event_tambahBtnActionPerformed

